                <br/><br/><strong>Un triangle</strong>
                <br/>J'ai trois angles dont la somme fait 180 degrés.
                Ce qui constitue la moitié d'un cercle qui lui a 360 degrés.

                Je suis un « triangle ».

                En musique le triangle est un instrument en forme de triangle, d'où son nom.
                Instrument en métal on frappe sur le triangle à l'aide d'une baguette également en métal et l'on obtient une note qui peut être très mélodieuse et agréable.
                le triangle ne se joue généralement pas seul mais est toujours intégré dans un ensemble de percussions lui-même pouvant être intégré dans un ensemble musical plus important comme un orchestre classique par exemple.
